    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblProduct.getModel();
        dtm.setRowCount(0);
        for(Product p: supplier.getSupplier_productCatalog().getProductCatalog()){
            Object[] col = new Object[3];
            col[0] = p;
            col[1] = p.getProductID();
            col[2] = p.getNumberInStock();
            dtm.addRow(col);
        }
    }

