    private void btnViewProductDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewProductDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProduct.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row from table first to update!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Product p = (Product)tblProduct.getValueAt(selectedRow, 0);
            ViewProductDetailsJPanel panel = new ViewProductDetailsJPanel(userProcessContainer,p);
            userProcessContainer.add("ViewProductDetailsJPanel", panel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }

        
    }//GEN-LAST:event_btnViewProductDetailsActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
       int selectedRow = tblProduct.getSelectedRow();
       if(selectedRow>=0){
           Product p = (Product)tblProduct.getValueAt(selectedRow,0);
           supplier.getSupplier_productCatalog().getProductCatalog().remove(p);
           populateTable();
       
       }else{
           JOptionPane.showMessageDialog(null, "Please select a row first!");
       }
    }//GEN-LAST:event_btnDeleteActionPerformed

