    private void btnUpdateUserAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateUserAccountActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
         int selectedRow=tblUserAccount.getSelectedRow();
        if(selectedRow<0){
        JOptionPane.showMessageDialog(null, "Please select row from table first to go ","Warning",JOptionPane.WARNING_MESSAGE );  
        return;
        }
        else{
        UserAccount userAccount=(UserAccount)tblUserAccount.getValueAt(selectedRow,0);
        UpdateUserAccountJPanel panel=new UpdateUserAccountJPanel(userProcessContainer,userAccount);
        userProcessContainer.add("UpdateUserAccountJPanel", panel);
        CardLayout cardLayout=(CardLayout)userProcessContainer.getLayout();
        cardLayout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnUpdateUserAccountActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
         int selectedRow=tblUserAccount.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select row from table first to go ","Warning",JOptionPane.WARNING_MESSAGE );
            return;
        }
        else{
            UserAccount userAccount=( UserAccount) tblUserAccount.getValueAt(selectedRow, 0);
            business.getBusiness_UserAccountDirectory().removeUserAccount(userAccount);
            populateTable();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed


