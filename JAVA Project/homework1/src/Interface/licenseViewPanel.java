     private void displayLicense (License license)
    {
      String licenseCarClass=license.getCarClass();
      carClassTextField.setText(licenseCarClass);
      
      String licenseAddress=license.getLicenseAddress();
      licenseAddressTextField.setText(licenseAddress);
      
      String licenseNumber=license.getLicenseNumber();
      licenseNumberTextField.setText(licenseNumber);
      
      String licenseState=license.getState();
      stateTextField.setText(licenseState);
      
      String licenseExpDate=license.getExpDate();
      expDateTextField.setText(licenseExpDate);
      
      
      
    }
     


