    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            String name = nameJTextField.getText();
            Boolean onlyOne = true;
            for (NetWork network : system.getNetworkList()) {
                if (network.getName().equals(name)) {
                    onlyOne = false;
                    break;
                }
            }

            if (onlyOne == false) {
                JOptionPane.showMessageDialog(null, "Please Enter Different Network!");
                return;
            } 
            
            if(name.equals("")){
             JOptionPane.showMessageDialog(null, "Invalid Input!");
             return;
            }
            
                NetWork network = system.createAndAddNetwork();
                network.setName(name);
                populateNetworkTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Incorrect Information!");
             return;
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblNetWork.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select row from table first to go ", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            NetWork network = (NetWork) tblNetWork.getValueAt(selectedRow, 0);
            system.getNetworkList().remove(network);
            populateNetworkTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

