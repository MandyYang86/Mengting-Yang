    private void nameJTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nameJTextFieldKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!(Character.isLetter(c) || (c == KeyEvent.VK_BACKSPACE)) || c == KeyEvent.VK_DELETE) {
            JOptionPane.showMessageDialog(null, "Please enter a text correctly!");
            evt.consume();
        }
    }//GEN-LAST:event_nameJTextFieldKeyTyped


