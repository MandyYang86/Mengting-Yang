    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int age=Integer.parseInt(AgeText.getText());
        if(GenderText.getText().equals("Femal")||"Male".equals(GenderText.getText())){
        if(age>0&&age<110){
        homeless.setName(nameText.getText());
        homeless.setGender(GenderText.getText());
        homeless.setAge(Integer.parseInt(AgeText.getText()));
        homeless.setEmailAddress(txtEmail.getText());
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
        JOptionPane.showMessageDialog(null, "Information Updated Created!"); 
            }else{
             JOptionPane.showMessageDialog(null, "Please enter the right Age! From 0~110!"); 
            }
        }else{  
            JOptionPane.showMessageDialog(null, "Please enter the right Gender! Select from Male or Female!"); 
        }
       
    }//GEN-LAST:event_btnSaveActionPerformed

    private void nameTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nameTextKeyTyped
        // TODO add your handling code here:
         char c=evt.getKeyChar();
         if(!(Character.isLetter(c) || (c==KeyEvent.VK_BACKSPACE)) || c==KeyEvent.VK_DELETE){
             JOptionPane.showMessageDialog(null, "Please enter a text correctly!"); 
          evt.consume();
         }
    }//GEN-LAST:event_nameTextKeyTyped

    private void AgeTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_AgeTextKeyTyped
        // TODO add your handling code here:
         char c=evt.getKeyChar();
         if(!(Character.isDigit(c) || (c==KeyEvent.VK_BACKSPACE)) || c==KeyEvent.VK_DELETE){
             JOptionPane.showMessageDialog(null, "Please enter a number correctly!"); 
          evt.consume();
         }
    }//GEN-LAST:event_AgeTextKeyTyped

