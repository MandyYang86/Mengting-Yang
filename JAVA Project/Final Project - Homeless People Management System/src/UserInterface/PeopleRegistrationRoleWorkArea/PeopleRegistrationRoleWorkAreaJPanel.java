    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow=homelessjTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select row from table first to go ","Warning",JOptionPane.WARNING_MESSAGE );
            return;
        }
        else{
            Homeless homeless=(Homeless)homelessjTable.getValueAt(selectedRow, 0);
            business.getHomelessDirectory().getHomelessList().remove(homeless);
            populatehomelessTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         int selectedRow = homelessjTable.getSelectedRow();
          if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from table to view details ", "Warning" ,JOptionPane.WARNING_MESSAGE);
            return;
        }
          else{
         Homeless homeless = (Homeless)homelessjTable.getValueAt(selectedRow, 0);
         ViewHomelessJPanel panel = new ViewHomelessJPanel(userProcessContainer,homeless);
          userProcessContainer.add(" ViewHomelessJPanel" , panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

