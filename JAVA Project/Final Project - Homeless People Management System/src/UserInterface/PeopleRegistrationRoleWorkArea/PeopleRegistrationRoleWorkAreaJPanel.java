    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = homelessjTable.getSelectedRow();
        Homeless homeless = (Homeless)homelessjTable.getValueAt(selectedRow, 0);
        JFileChooser imgc = new JFileChooser();
        imgc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int i = imgc.showOpenDialog(null);
        String path = "";
        if (i == imgc.APPROVE_OPTION) {
            path = imgc.getSelectedFile().getAbsolutePath();
        }
        picturelable.setIcon(new ImageIcon(path));
        homeless.setIconPath(path);
    }//GEN-LAST:event_jButton1ActionPerformed

