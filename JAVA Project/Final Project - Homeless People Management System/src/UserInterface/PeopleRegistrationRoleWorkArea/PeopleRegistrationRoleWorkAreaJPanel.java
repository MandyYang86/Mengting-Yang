     public void populatehomelessTable(){
        DefaultTableModel model = (DefaultTableModel) homelessjTable.getModel();
        
        model.setRowCount(0);
        
        for (Homeless homeless : business.getHomelessDirectory().getHomelessList()){
            Object[] row = new Object[5];
            row[0] = homeless;
            row[1] = homeless.getID();
            row[2] = homeless.getGender();
            row[3] = homeless.getAge();
            row[4] = homeless.getEmailAddress();
            model.addRow(row);
        }
    }
    

