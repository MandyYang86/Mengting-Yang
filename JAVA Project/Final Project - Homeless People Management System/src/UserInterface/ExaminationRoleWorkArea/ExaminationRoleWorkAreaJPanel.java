    public void  populateAppiontmentTable() {
       DefaultTableModel model = (DefaultTableModel) tblApp.getModel();

        model.setRowCount(0);
        for (Appointment app:business.getAppointmentDirectory().getAppointmentDirectory()) {
            if(app.getHospitalName().equals(enterprise.getName())){

            Object[] row = new Object[7];
            row[0] = app.getHospitalName();
            row[1] = app.getShelterName();
            row[2] = app.getYear();
            row[3] = app.getMonth();
            row[4] = app.getDate();
            row[5] = app.getStatus();
            row[6] = app;

            model.addRow(row);

            }

        }
    }
    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tblWorkRequest.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request: account.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[4];
            row[0] = request.getMessage();
            row[1] = request.getSender();
            row[2] = request.getReceiver();
            row[3] = request.getStatus();
           
            model.addRow(row);
        }
        
    }
    
    public void populateEmailTable(){
        DefaultTableModel model = (DefaultTableModel) tblEmail.getModel();
        
        model.setRowCount(0);
        for (Homeless homeless:business.getHomelessDirectory().getHomelessList()){
            Object[] row = new Object[2];
            row[0] = homeless;
            row[1] = homeless.getEmailAddress();
           
            model.addRow(row);
        }
        
    }

