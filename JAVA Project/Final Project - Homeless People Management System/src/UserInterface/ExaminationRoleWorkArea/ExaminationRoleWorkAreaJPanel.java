    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblApp.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from table to go ", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Appointment appointment=(Appointment) tblApp.getValueAt(selectedRow, 6);
        appointment.setStatus("Complete");
        JOptionPane.showMessageDialog(null, "Process Successfully! ", "Warning", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnProcessActionPerformed

    private void btnSendEmailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendEmailActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblEmail.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from table to go ", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String emailAddress=(String) tblEmail.getValueAt(selectedRow, 1);
        business.getSendEmail().setPersonalName(enterprise.getName());
        business.getSendEmail().setMail_to(emailAddress);
    try {
        business.getSendEmail().send();
    } catch (Exception ex) {
        Logger.getLogger(ExaminationRoleWorkAreaJPanel.class.getName()).log(Level.SEVERE, null, ex);
    }
        JOptionPane.showMessageDialog(null, "Send Successfully! ", "Warning", JOptionPane.WARNING_MESSAGE);
        
    }//GEN-LAST:event_btnSendEmailActionPerformed

