     public void populatevaccineTable(){
        DefaultTableModel model = (DefaultTableModel) vaccineJTable.getModel();
        
        model.setRowCount(0);
        
        for (Vaccine vaccine : business.getVaccineDirectory().getVaccineDirectory()){
            Object[] row = new Object[4];
            row[0] = vaccine;
            row[1] = vaccine.getQuantity();
            row[2] = vaccine.getPrice();
            row[3] = vaccine.getDisease().getName();
            model.addRow(row);
        }
    }
     private void populatediseaseComboBox(){
        jComboBox1.removeAllItems();
        
        for (Disease disease : business.getDiseaseDirectory().getDiseaseDirectory()){
            jComboBox1.addItem(disease);
        }
    }
