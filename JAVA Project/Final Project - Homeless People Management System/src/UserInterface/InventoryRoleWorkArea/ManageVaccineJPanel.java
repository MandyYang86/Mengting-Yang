    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow=vaccineJTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select row from table first to go ","Warning",JOptionPane.WARNING_MESSAGE );
            return;
        }
        else{
            Vaccine vaccine=(Vaccine)vaccineJTable.getValueAt(selectedRow, 0);
           business.getVaccineDirectory().getVaccineDirectory().remove(vaccine);
        
            populatevaccineTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

