    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow=diseaseJTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select row from table first to go ","Warning",JOptionPane.WARNING_MESSAGE );
            return;
        }
        else{
            Disease disease=(Disease)diseaseJTable.getValueAt(selectedRow, 0);
            business.getDiseaseDirectory().getDiseaseDirectory().remove(disease);
             populatediseaseTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

