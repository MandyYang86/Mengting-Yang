    public void populatediseaseTable(){
        DefaultTableModel model = (DefaultTableModel) diseaseJTable.getModel();

        model.setRowCount(0);
        for (Disease disease : business.getDiseaseDirectory().getDiseaseDirectory()) {
            Object[] row = new Object[1];
            row[0] = disease;
            model.addRow(row);
        }
    }

