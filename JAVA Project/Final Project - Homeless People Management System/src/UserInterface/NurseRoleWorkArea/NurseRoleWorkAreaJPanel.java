    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblNurse.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a row first!");
            return; 
        }
        
        WorkRequestPEToLab request = (WorkRequestPEToLab) tblNurse.getValueAt(selectedRow, 2);
        Vaccine vaccine = (Vaccine) tblNurse.getValueAt(selectedRow, 3);
        int newNumber=vaccine.getQuantity()-1;
        vaccine.setQuantity(newNumber);
                
        request.setReceiver(userAccount);
        request.setStatus("VaccineCompleted");
        populateTable();
         
        DefaultTableModel model = (DefaultTableModel)tblVaccine.getModel();
        model.setRowCount(0);
        Object[] row = new Object[3];
        row[0] = vaccine;
        row[1] = vaccine.getQuantity();
        row[2] = vaccine.getPrice();
        model.addRow(row);
        
    }//GEN-LAST:event_btnProcessActionPerformed

