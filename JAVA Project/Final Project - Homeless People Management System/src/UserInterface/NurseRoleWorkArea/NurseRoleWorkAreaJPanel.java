    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblNurse.getModel();
        model.setRowCount(0);
        for(WorkRequest workrequest : nurse.getWorkQueue().getWorkRequestList()){
            if (workrequest instanceof Business.WorkQueue.WorkRequestPEToLab) {
                WorkRequestPEToLab request = (WorkRequestPEToLab) workrequest;
                Object[] row = new Object[5];
                row[0] = request.getHomeless().getName();
                row[1] = request.getHomeless().getID();
                row[2] = request;
                row[3] = request.getVaccine();
                row[4] = request.getStatus();
                model.addRow(row);
            }
    }
    }

